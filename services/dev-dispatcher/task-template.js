'use strict';

/**
 * generateTaskInstruction
 *
 * Wraps a project's brief.md content in a structured prompt that
 * Claude Code (or Codex) can execute directly.
 *
 * The file is ONLY written — it is NOT executed automatically.
 * A human must run `claude` (or equivalent) against it.
 *
 * @param {object} opts
 * @param {number}  opts.projectId
 * @param {string}  opts.slug
 * @param {string}  opts.title
 * @param {string}  opts.workspacePath  - absolute path inside container, e.g. /projects/3-my-app
 * @param {string}  opts.briefContent   - raw contents of brief.md
 * @param {string}  opts.dispatchedAt   - ISO timestamp
 * @returns {string} markdown task instruction
 */
function generateTaskInstruction({ projectId, slug, title, workspacePath, briefContent, dispatchedAt }) {
  return `# Dev Agent Task — ${title}

> **Dispatched**: ${dispatchedAt}
> **Project ID**: ${projectId}
> **Slug**: ${slug}
> **Workspace**: \`${workspacePath}\`

---

## Instructions for Claude Code / Codex

You are the development agent for this project.
Work ONLY inside the workspace directory listed above.
Do NOT push to git, send messages, or contact the client — those steps require human approval.

### Workflow

1. Read \`brief.md\` (reproduced below) and \`scope.md\` to understand deliverables.
2. Read \`todo.md\` for the task checklist.
3. Implement each task in order, marking items done in \`todo.md\` as you go (\`[ ]\` → \`[x]\`).
4. Write clean, minimal code — do not over-engineer.
5. When finished, write a short summary to \`delivery-notes.md\` in the workspace.
6. Stop. A human will review before anything is sent to the client.

### Constraints

- No automatic git commits or pushes.
- No external API calls beyond what the brief explicitly requires.
- No modifications outside \`${workspacePath}/\`.

---

## brief.md

${briefContent}

---

*This file was generated by the Dev Agent Dispatcher service.*
*Human review is required before delivery.*
`;
}

module.exports = { generateTaskInstruction };
